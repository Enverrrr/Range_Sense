/**
  * @brief This function handles UART4 global interrupt / UART4 wake-up interrupt through EXTI line 34.
  */
void UART4_IRQHandler(void)
{
  /* USER CODE BEGIN UART4_IRQn 0 */

  /* USER CODE END UART4_IRQn 0 */
  HAL_UART_IRQHandler(&huart4);
  /* USER CODE BEGIN UART4_IRQn 1 */
  if((UART4->ISR & USART_ISR_RXNE) && !(UART4->ISR & (USART_ISR_PE |
  		  USART_ISR_FE |
  		  USART_ISR_NE |
  		  USART_ISR_ORE))){

  	  receive_data=UART4->RDR;            // Receive data (1 byte)
  	//  HAL_UART_Transmit(&huart4,&receive_data,1,100);

  	  if((receive_data==0xFF)&&(package_state==0)){
  		  data_index = 0;
  		  Range_data[data_index]=receive_data;
  		  data_index++;
  		  package_state=1;
  		  data_valid=0;
  	  }else{
  		  Range_data[data_index]=receive_data;
  		  if(data_index==3){
  			  data_index=0;
  			  my_sum=Range_data[0]+Range_data[1]+Range_data[2];
  			  if (my_sum==Range_data[3]){
  				  data_valid=1;
  				  package_state=0;
  			  }
  		  }
  		  data_index++;
  		 // HAL_UART_Transmit(&huart4, &data_index, 1, 100);
  	  }
    }

    UART4->RDR = 0x00;

  /* USER CODE END UART4_IRQn 1 */
}

